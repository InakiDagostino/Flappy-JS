function checkCollision() {
  return bird.shape.find((row, y) => {
    return row.find((value, x) => {
      return (
        value !== 0 && board[y + bird.position.y]?.[x + bird.position.x] !== 0
      );
    });
  });
}

function checkPipeCollision() {
  return pipesArr.find((pipe) => {
    return pipe.shape.find((row, y) => {
      return row.find((value, x) => {
        return (
          pipe.position.x + x === bird.position.x + 3 &&
          pipe.position.y + y === bird.position.y + 3 &&
          value !== 0 &&
          value !== 2
        );
      });
    });
  });
}

function checkPipePoints() {
  return pipesArr.find((pipe) => {
    return pipe.shape.find((row, y) => {
      return row.find((value, x) => {
        return (
          pipe.position.x + x === bird.position.x + 3 &&
          pipe.position.y + y === bird.position.y + 3 &&
          value === 2
        );
      });
    });
  });
}

